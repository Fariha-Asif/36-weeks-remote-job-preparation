/* Write a JavaScript program to print all natural numbers from 1 to n using a while loop.

Take the value of n as input from the user.

Print each number on a new line.

*/

let n = 10;  // in node we can't take prompt

let i = 0;

while(i <= n){
    console.log(i)
    i++
}

/* Write a JavaScript program to print all natural numbers in reverse (from n to 1) using a while loop.

Take the value of n as input from the user.

Print each number on a new line in reverse order.

*/
while(n >= i) {
    console.log(n)
    n--;
}
